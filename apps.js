var car = 20;

var crash = 40;

function addCrash(crash,car){
    return crash + car
}

console.log(addCrash(crash,car))



function subCar(car,crash){
    return car - crash
}

console.log(subCar(car,crash))

function mulCrash(car,crash){
    return car * crash
}

console.log(mulCrash(car,crash))

function divCar(car,crash){
    return car / crash
}

console.log(divCar(car,crash))